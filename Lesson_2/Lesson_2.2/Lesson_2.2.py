# Print command
print('We learn Python')

# Printable text must be in quote
print('Python')
print("Python")

# Print can take multiple arguments
print('Soon', 'i will be', 'programming on', 'Python!')

# Command Print() add space between arguments
print('1', '2', '4', '8', '16')

# Each command Print() prints text on new line
print('Which good day!')
print('To work me not lazy')

# Command Print() without arguments insert a new line
print('Which good day!')
print()
print('To work me not lazy')

# How use single and double quotes
print('In text are used "double" quotes')
print("In text are used 'single' quotes")

# Пример использования input()
print('Как тебя зовут?')
name = input()
print('Привет,', name)

# Текст приглашения моно добавлять в саму команду ввода текста
name = input('Как тебя зовут?')
print('Привет,', name)
