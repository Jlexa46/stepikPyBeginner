# Целочисленный тип данных

# Явное указание численного значения в коде программы называется целочисленным литеролом.
n = 17  # целочисленный литерал
m = 7   # целочисленный литерал

# Преобразование строки в целое число
num = int(input())  # преобразование строки в цело число
n = int('12345')    # преобразование строки в цело число

# Целочисленные операторы
a, b = 13, 7
total = a + b
diff = a - b
prod = a * b
div1 = a / b
div2 = a // b
mod = a % b
exp = a ** b

print(a, '+', b, '=', total)    # 13 + 7 = 20
print(a, '-', b, '=', diff)     # 13 - 7 = 6
print(a, '*', b, '=', prod)     # 13 * 7 = 91
print(a, '/', b, '=', div1)     # 13 / 7 = 1.8571428571428572
print(a, '//', b, '=', div2)    # 13 // 7 = 1
print(a, '%', b, '=', mod)      # 13 % 7 = 6
print(a, '**', b, '=', exp)     # 13 ** 7 = 62748517

# Длинная арифметика
atom = 10 ** 80 # количество атомов во вселенной
